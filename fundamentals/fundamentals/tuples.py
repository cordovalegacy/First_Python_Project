# TUPLE = IMMUTABLE LIST

# tupples use parenthesis()
# examples:
tuple_data = ('physics', 'chemistry', 1997, 2000)
tuple_num = (1, 2, 3, 4, 5 )
tuple_letters = "a", "b", "c", "d"

print(tuple_data)
print(tuple_data + tuple_num)
print(tuple_data + tuple_letters)
print(tuple_data + tuple_letters + tuple_num)